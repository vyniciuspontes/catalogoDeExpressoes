    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed

        if (loginTextField.getText().trim().isEmpty() || passwordField.getPassword().length < 0) {
            JOptionPane.showMessageDialog(LoginDialog.this, "Preencha todos os campos", "Campos vazios encontrados",
                    JOptionPane.WARNING_MESSAGE);
            return;
        }
        String password = String.copyValueOf(passwordField.getPassword());
        String nomeUsuario = LoginController.getInstance().fazerLogin(loginTextField.getText(), password);

        if (nomeUsuario != null) {
            JOptionPane.showMessageDialog(LoginDialog.this, "Bem vindo " + nomeUsuario, "Sucesso",
                    JOptionPane.INFORMATION_MESSAGE);
            adm.loginSucedido(nomeUsuario);
            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(LoginDialog.this, "Credenciais invÃ¡lidas", "Erro",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_loginButtonActionPerformed

